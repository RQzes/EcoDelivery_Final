public void listar() {

    ArrayList<Pedido> lista = dao.listar();

    modelo = (DefaultTableModel) tblPedidos.getModel();
    modelo.setRowCount(0);

    Object[] fila = new Object[5];

    for (int i = 0; i < lista.size(); i++) {

        fila[0] = lista.get(i).getCodigo();
        fila[1] = lista.get(i).getDescripcion();
        fila[2] = lista.get(i).getDistancia();
        fila[3] = lista.get(i).getCodigoCliente();
        fila[4] = lista.get(i).getEstado();

        modelo.addRow(fila);
    }

    tblPedidos.setModel(modelo);
}

