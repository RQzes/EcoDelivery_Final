    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
    int fila = tblEntregas.getSelectedRow();

    if (fila < 0) {
        JOptionPane.showMessageDialog(this, "Seleccione una entrega de la tabla");
        return;
    }

    int codigo = Integer.parseInt(tblEntregas.getValueAt(fila, 0).toString());

    int opcion = JOptionPane.showConfirmDialog(
            this,
            "¿Desea eliminar esta entrega?",
            "Eliminar entrega",
            JOptionPane.YES_NO_OPTION
    );

    if (opcion == JOptionPane.YES_OPTION) {

        boolean ok = dao.eliminar(codigo);

        if (ok) {
            JOptionPane.showMessageDialog(this, "Entrega eliminada correctamente");
            listar();
            limpiar();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo eliminar la entrega");
        }
    }

} catch (Exception ex) {
    JOptionPane.showMessageDialog(this, "Error al eliminar: " + ex.getMessage());
}        // TODO add your handling code here:
    }//GEN-LAST:event_btnEliminarActionPerformed

