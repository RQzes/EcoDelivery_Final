public void listar() {

    ArrayList<Entrega> lista = dao.listar();

    modelo = (DefaultTableModel) tblEntregas.getModel();
    modelo.setRowCount(0);

    Object[] fila = new Object[7];

    for (int i = 0; i < lista.size(); i++) {

        fila[0] = lista.get(i).getCodigo();
        fila[1] = lista.get(i).getFecha();
        fila[2] = lista.get(i).getTiempo();
        fila[3] = lista.get(i).getCo2();
        fila[4] = lista.get(i).getCodigoVehiculo();
        fila[5] = lista.get(i).getCodigoPedido();
        fila[6] = lista.get(i).getEstado();

        modelo.addRow(fila);
    }

    tblEntregas.setModel(modelo);
}

