    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
        if (!validarCampos()) {
            return;
        }

        Usuario u = new Usuario();

        u.setNombre(txtNombre.getText().trim());
        u.setUsuario(txtUsuario.getText().trim());
        u.setClave(new String(txtClave.getPassword()).trim());
        u.setRol(cboRol.getSelectedItem().toString());

        boolean ok = dao.insertar(u);

        if (ok) {
            JOptionPane.showMessageDialog(this, "Usuario registrado correctamente");
            limpiar();

            Login login = new Login();
            login.setVisible(true);
            this.dispose();

        } else {
            JOptionPane.showMessageDialog(this, "No se pudo registrar el usuario");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al registrar: " + e.getMessage());
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnRegistrarActionPerformed

