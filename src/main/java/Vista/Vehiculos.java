public void listar() {

    ArrayList<Vehiculo> lista = dao.listar();

    modelo = (DefaultTableModel) tblVehiculos.getModel();
    modelo.setRowCount(0);

    Object[] fila = new Object[5];

    for (int i = 0; i < lista.size(); i++) {

        fila[0] = lista.get(i).getCodigo();
        fila[1] = lista.get(i).getTipo();
        fila[2] = lista.get(i).getCapacidad();
        fila[3] = lista.get(i).getVelocidad();
        fila[4] = lista.get(i).getEstado();

        modelo.addRow(fila);
    }

    tblVehiculos.setModel(modelo);
}

